-- CREATE USEFUL VIEWS

-- View: Monthly Sales Summary
CREATE OR REPLACE VIEW monthly_sales_summary AS
SELECT 
    MONTHNAME(Month) AS MonthName,
    Year,
    COUNT(DISTINCT NameOfItem) AS TotalProducts,
    SUM(SaleQty) AS TotalQuantitySold,
    ROUND(SUM(Amt), 2) AS TotalAmount,
    ROUND(SUM(`Amt+GST`), 2) AS TotalAmountWithGST
FROM martdatabase
GROUP BY MONTHNAME(Month), Year, MONTH(Month)
ORDER BY Year DESC, MONTH(Month) DESC;

-- View: Top Products
CREATE OR REPLACE VIEW top_products AS
SELECT 
    NameOfItem,
    Code,
    SUM(SaleQty) AS TotalSold,
    ROUND(SUM(`Amt+GST`), 2) AS TotalRevenue
FROM martdatabase
GROUP BY NameOfItem, Code
ORDER BY TotalRevenue DESC
LIMIT 20;

-- View: Category-wise Sales (based on code prefix)
CREATE OR REPLACE VIEW category_sales AS
SELECT 
    CASE 
        WHEN Code LIKE 'PC%' THEN 'Personal Care'
        WHEN Code LIKE 'HC%' THEN 'Hair Care'
        WHEN Code LIKE 'GR%' THEN 'Groceries'
        WHEN Code LIKE 'BV%' THEN 'Beverages'
        WHEN Code LIKE 'SN%' THEN 'Snacks'
        WHEN Code LIKE 'DR%' THEN 'Dairy'
        WHEN Code LIKE 'HH%' THEN 'Household'
        ELSE 'Other'
    END AS Category,
    COUNT(DISTINCT NameOfItem) AS ProductCount,
    SUM(SaleQty) AS TotalQuantity,
    ROUND(SUM(`Amt+GST`), 2) AS TotalRevenue
FROM martdatabase
GROUP BY Category
ORDER BY TotalRevenue DESC;

